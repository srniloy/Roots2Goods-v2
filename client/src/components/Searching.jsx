const Searching = () => {

  let startedYear = 2009;
  const [page, setPage] = React.useState(0);
  const [rowsPerPage, setRowsPerPage] = React.useState(8);

  const handleChangePage = (event, newPage) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (event) => {
    setRowsPerPage(+event.target.value);
    setPage(0);
  };

  const [locations, setLocations] = React.useState([]);
  const [products, setProducts] = React.useState([]);

  const fetchLocations = async()=>{
    const postData = {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
    };

    const res = await fetch(
      '/api/get/get_locations',
      postData
    )
    const response = await res.json()
    
    let tempLocations = []
    response.data.map(data =>{
      tempLocations.push({label: data.address})
    })
    setLocations(tempLocations)
  }

  const fetchProducts = async()=>{
    const postData = {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
    };

    const res = await fetch(
      '/api/get/get_product_names',
      postData
    )
    const response = await res.json()
    console.log(response)
    let tempProducts = []
    response.data.map(data =>{
      tempProducts.push({label: data.product_name})
    })
    setProducts(tempProducts)
  }

  React.useEffect(()=>{
    fetchLocations()
    fetchProducts()
  }, [])
  

  return (
    <div className="frmr-trader-searching-container">
      <Stack direction='row' justifyContent='left' alignItems='end' gap={5} marginTop='50px'>
        <Autocomplete
          disablePortal
          id="combo-box-demo"
          options={locations}
          sx={{ width: 300 }}
          renderInput={(params) => <TextField {...params} variant='filled' label="Location" />}
        />
        {/* <Autocomplete
          disablePortal
          id="combo-box-demo"
          options={products}
          sx={{ width: 300 }}
          renderInput={(params) => <TextField {...params} variant='filled' label="Product" />}
        /> */}


        <Button variant="contained" sx={{backgroundColor: "var(--yellow)", height: '40px', fontFamily:'Gothicb'}} startIcon={<PersonSearchIcon fontSize="large" />}>
          Search
        </Button>
      </Stack>


      <Paper sx={{ width: '100%', overflow: 'hidden', margin: '50px 0', backgroundColor: "transparent" }}>
      <TableContainer sx={{ maxHeight: 500, minHeight: 500 }}>
        <Table stickyHeader aria-label="sticky table">
          <TableHead>
            <TableRow>
              {columns.map((column) => (
                <TableCell
                  key={column.id}
                  align={column.align}
                  style={{ minWidth: column.minWidth, backgroundColor: "rgba(36, 68, 65, 0.946)" }}
                >
                  {column.label}
                </TableCell>
              ))}
            </TableRow>
          </TableHead>
          <TableBody>
            {Data.traderSearch
              .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
              .map((row) => {
                return (
                  <TableRow hover role="checkbox" tabIndex={-1} key={row.code}>
                    {columns.map((column) => {
                      const value = row[column.id];
                      return (
                        <TableCell key={column.id} align={column.align}>
                          {column.format && typeof value === 'number'
                            ? column.format(value)
                            : value}
                        </TableCell>
                      );
                      
                    })}
                  </TableRow>
                );
              })}
          </TableBody>
        </Table>
      </TableContainer>
      <TablePagination
        rowsPerPageOptions={[8, 10, 25, 100]}
        component="div"
        count={rows.length}
        rowsPerPage={rowsPerPage}
        page={page}
        onPageChange={handleChangePage}
        onRowsPerPageChange={handleChangeRowsPerPage}
      />
    </Paper>

    </div>
  )
}


